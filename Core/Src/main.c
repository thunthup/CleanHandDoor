const float speedOfSound = 0.0343/2;
float distance;
char uartBuf[100];
int raw;
char msg[20];
uint32_t numTicks;
int currentAlcohol;
int currentDistance;

int getAlcohol(int n){
	HAL_ADC_Start(&hadc1);
		  HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);
		  raw = HAL_ADC_GetValue(&hadc1)-n;

		  sprintf(msg,"%d \r\n",raw);
		  HAL_UART_Transmit(&huart2, msg, strlen(msg), HAL_MAX_DELAY);
		  HAL_Delay(100);
		  return raw;
}

float getDistance(){
	 HAL_GPIO_WritePin(Trig_GPIO_Port, Trig_Pin, GPIO_PIN_RESET);
		  usDelay(3);

		  HAL_GPIO_WritePin(Trig_GPIO_Port, Trig_Pin, GPIO_PIN_SET);
		  usDelay(10);
		  HAL_GPIO_WritePin(Trig_GPIO_Port, Trig_Pin, GPIO_PIN_RESET);

		  while(HAL_GPIO_ReadPin(Echo_GPIO_Port, Echo_Pin) == GPIO_PIN_RESET);
		  numTicks = 0 ;
		  while(HAL_GPIO_ReadPin(Echo_GPIO_Port, Echo_Pin) == GPIO_PIN_SET){
			  numTicks++;
			  usDelay(2);
		  }

		  distance = (numTicks + 0.0f) * 2.8 * speedOfSound;

		  sprintf(uartBuf,"Distance(cm): %.1f \r\n ",distance);
		  HAL_UART_Transmit(&huart2, (uint8_t *)uartBuf, strlen(uartBuf), 100);
		  HAL_Delay(100);
		  return distance;
}

void openDoor(){
	sprintf(uartBuf,"o");
	HAL_UART_Transmit(&huart1, (uint8_t *)uartBuf, strlen(uartBuf), 100);
	HAL_UART_Transmit(&huart2, (uint8_t *)uartBuf, strlen(uartBuf), 100);
	sprintf(uartBuf,"i");
	HAL_UART_Transmit(&huart1, (uint8_t *)uartBuf, strlen(uartBuf), 100);
	HAL_UART_Transmit(&huart2, (uint8_t *)uartBuf, strlen(uartBuf), 100);
	HAL_GPIO_TogglePin(GPIOA,GPIO_PIN_5);
	htim2.Instance->CCR1 = 112;
	HAL_Delay(4000);
	htim2.Instance->CCR1 = 35;
	HAL_Delay(1000);
	HAL_GPIO_TogglePin(GPIOA,GPIO_PIN_5);


	sprintf(uartBuf,"c");
		HAL_UART_Transmit(&huart1, (uint8_t *)uartBuf, strlen(uartBuf), 100);
		HAL_UART_Transmit(&huart2, (uint8_t *)uartBuf, strlen(uartBuf), 100);
}
